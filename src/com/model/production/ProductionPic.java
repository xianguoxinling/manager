package com.model.production;

public class ProductionPic
{
    private String id = null;
    private String productionUUID = null;
    private String magicKey = null;
    private String path = null;
    
    public String getId()
    {
        return id;
    }
    public void setId(String id)
    {
        this.id = id;
    }
    public String getProductionUUID()
    {
        return productionUUID;
    }
    public void setProductionUUID(String productionUUID)
    {
        this.productionUUID = productionUUID;
    }
    public String getMagicKey()
    {
        return magicKey;
    }
    public void setMagicKey(String magicKey)
    {
        this.magicKey = magicKey;
    }
    public String getPath()
    {
        return path;
    }
    public void setPath(String path)
    {
        this.path = path;
    }
}
